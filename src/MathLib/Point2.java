package MathLib;

/**
 * Point in 2 dimensional space
 */
public class Point2 {
    /**
     * x coordinate
     */
    public final double x;

    /**
     * y coordinate
     */
    public final double y;

    /**
     * constructor
     * @param x x
     * @param y y
     */
    public Point2(final double x, final double y){
        this.x = x;
        this.y = y;
    }


}
